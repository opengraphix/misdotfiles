brew install nmap
brew install pngcheck
brew install socat
brew install sqlmap
brew install tcpflow
brew install tcpreplay
brew install tcptrace
brew install ucspi-tcp # `tcpserver` etc.
#brew install xpdf
brew install xz

# Install other useful binaries.
brew install ack
#brew install dark-mode
#brew install exiv2
brew install tmux
brew install git
brew install git-lfs
